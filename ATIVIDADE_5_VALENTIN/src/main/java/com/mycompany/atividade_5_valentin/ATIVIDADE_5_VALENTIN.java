/*
 * Contagem Regressiva

   Um dos sistemas de alertas da empresa exige que uma contagem regressiva seja feita para avisar os usuários sobre a finalização de um processo.
 */

package com.mycompany.atividade_5_valentin;

/**
 *
 * @author v.gomes
 */
public class ATIVIDADE_5_VALENTIN {

    public static void main(String[] args) {    
        for(int c = 5; c >= 0; c--){
            System.out.println(c);
        }
        System.out.println("Processo finalizado ");
    }
}
